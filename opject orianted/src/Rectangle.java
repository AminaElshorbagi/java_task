public class Rectangle {
    private double Length ;
    private double Width;
    public Rectangle(){
        Length=10.5;
        Width=9;
        System.out.println("AAAAAAAA");
    }
public void setlength(double l ){
        Length=l;
}
    public void setwidth(double w )
    {
        Width=w;

    }
    public double getLength ( ){
       return Length;

    }
    public double getWidth( ){
      return Width;

    }
}
